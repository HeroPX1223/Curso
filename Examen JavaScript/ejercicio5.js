let diaSemanana = 3
switch (diaSemanana) {
    case 1:
        console.log("Lunes: Lentejas");   
        break;
    case 2:
        console.log("Martes: Pollo al horno"); 
        break;
    case 3:
        console.log("Miércoles: Pescado a la plancha");
        break;
    case 4:
        console.log("Jueves: Pasta carbonara");
        break;
    case 5:
        console.log("Viernes: Paella");
        break;
    default:
        console.log("Día no válido. Introduce un número del 1 al 5.");
}