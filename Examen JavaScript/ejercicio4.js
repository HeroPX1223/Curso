let colorSemaforo = "verde"
if (colorSemaforo === "verde") {
    console.log("El semáforo está en verde, puedes cruzar.")
}
else if (colorSemaforo === "amarillo") {
    console.log("El semáforo está en amarillo, reduzca la velocidad.")
}
else if (colorSemaforo === "rojo") {
    console.log("El semáforo está en rojo, debes detenerte.")
}
else {
    console.log("Color de semáforo no reconocido.")
}